// models/PrivacyPolicy.js
const mongoose = require('mongoose');

const PrivacyPolicySchema = new mongoose.Schema({
    title: {
        type: String,
        required: true,
        trim: true,
    },
    description: {
        type: String,
        required: true,
        trim: true,
    },
    createdAt: {
        type: Date,
        default: Date.now,
    },
    updatedAt: {
        type: Date,
        default: Date.now,
    }
});

module.exports = mongoose.model('PrivacyPolicy', PrivacyPolicySchema);
